import Document, {
  Html,
  Head,
  Main,
  NextScript,
  DocumentContext,
} from "next/document";

class MyDocument extends Document {
  static async getInitialProps(ctx: DocumentContext) {
    const initialProps = await Document.getInitialProps(ctx);
    return { ...initialProps };
  }

  render() {
    return (
      <Html>
        <Head>
          <meta
            name="description"
            content="We offer a wide range of web-based services (Static and Dynamic website, custom website development, web services module designing and Maintainance)"
          />
          <meta
            name="keywords"
            content="website development, static website, dynamic web services, eCommerce website, portfolio design, UI design, UX design"
          />
        </Head>
        <body>
          <Main />
          <NextScript />
        </body>
      </Html>
    );
  }
}

export default MyDocument;
